package com.example.pixaloop.common.render.utils;

public class SizeF {
    public float a = 0.0f;
    public float b = 0.0f;

    public float a() {
        return this.b;
    }

    public float b() {
        return this.a;
    }

    public String toString() {
        return "[" + this.a + "x" + this.b + "]";
    }
}
